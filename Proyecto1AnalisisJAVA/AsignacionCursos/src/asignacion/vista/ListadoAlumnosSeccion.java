    public void mostrarlistado(String curso, String seccion){
    DefaultTableModel modelo = new DefaultTableModel();
    modelo.addColumn("Id Asignacion");
    modelo.addColumn("Id Curso");
    modelo.addColumn("Id Alumno");
    modelo.addColumn("Nombres");
    modelo.addColumn("Apellidos");
    modelo.addColumn("Seccion");
    modelo.addColumn("Fecha");
    Tbl_listadoalumnosseccion.setModel(modelo);
    
    String codsql = null;
    String cont;
    cont = "SELECT COUNT(idCurso) FROM cursos where idCurso   = '"+txt_idcurso+"'";
    Connection cn = null;
    
    ResultSet rs = null;
    
   ;
    
    try{
    if(Integer.parseInt(cont)== 1){
       
        codsql = "SELECT A.idAsinacion, B.idCurso, C.idAlumno, C.nombres, C.apellidos, A.fecha, B.seccion FROM asignacionalumno A INNER JOIN cursos B INNER JOIN alumno C WHERE idCurso= '"+curso+"', seccion='"+seccion+"'";
    }
    }catch (NumberFormatException nfe){
        JOptionPane.showMessageDialog(null, nfe);
    }
    return cont;
    
    
    String []datos = new String[7];
    
    try{
        java.sql.Statement leer =  conect.createStatement();
        ResultSet resultado = leer.executeQuery(codsql);
        
        while(resultado.next()){
            datos[0] = resultado.getString(1);
            datos[1] = resultado.getString(2);
            datos[2] = resultado.getString(3);
            datos[3] = resultado.getString(4);
            datos[4] = resultado.getString(5);
            datos[5] = resultado.getString(6);
            datos[6] = resultado.getString(7);
            modelo.addRow(datos);
        }
        Tbl_listadoalumnosseccion.setModel(modelo);
        
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, e + "Error en la consulta");
    
    }
    
     
    }


